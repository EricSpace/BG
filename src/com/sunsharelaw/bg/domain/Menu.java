package com.sunsharelaw.bg.domain;

public class Menu {
	private int row_id;
	private String company_id;
	private String menu_id;
	private String parent_flag;
	private String name;
	private String type;
	private String key;
	private String url;
	private String resp_type;
	private String parent_menu_id;
	public int getRow_id() {
		return row_id;
	}
	public void setRow_id(int row_id) {
		this.row_id = row_id;
	}
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	public String getMenu_id() {
		return menu_id;
	}
	public void setMenu_id(String menu_id) {
		this.menu_id = menu_id;
	}
	public String getParent_flag() {
		return parent_flag;
	}
	public void setParent_flag(String parent_flag) {
		this.parent_flag = parent_flag;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getResp_type() {
		return resp_type;
	}
	public void setResp_type(String resp_type) {
		this.resp_type = resp_type;
	}
	public String getParent_menu_id() {
		return parent_menu_id;
	}
	public void setParent_menu_id(String parent_menu_id) {
		this.parent_menu_id = parent_menu_id;
	}


}
